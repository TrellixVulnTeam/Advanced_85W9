public class WeeData {
    private int p;
    private int b;

    public WeeData(int p, int b) {
        this.p = p;
        this.b = b;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
